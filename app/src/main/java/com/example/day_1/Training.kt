package com.example.day_1

fun main () {
    //Val value cannot change while var value can change
    val name = "Daniel"
    println("My name is "+name)
    //OR
    println("My name is $name")

    val age:Int = 20
    println("I am $age")

    val sum:Int = 10+20
    println("The Answer is $sum")

    val product:Int = 2*4
    println("The Answer is $product")

    val divide:Int = 90/10
    println("The Answer is $divide")

    val isSunny:Boolean = true

}
